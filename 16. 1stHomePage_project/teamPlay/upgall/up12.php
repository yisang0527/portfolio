                <h3>책 소개</h3>
                <p>머리가 좋아지는 말랑말랑 공부 습관<br>
신나는 공부 게임, 어휘 스도쿠로<br>
일상생활부터 학교 공부에 필요한 어휘를 한 번에!<br>

어휘력이 부족한 어린이들은 생활 속에서 예상치 못한 상황을 경험하기 쉽습니다. 친구에게 솔직하게 기분을 표현했다가 얼굴을 붉히기도 하고, 선생님한테 숙제를 깜빡한 이유에 대해 설명하다가 말이 뒤엉켜 버리기도 하지요. 이때, 어휘의 이미와 쓰임을 제대로 알면 하기 어렵거나 듣기 싫은 말도 간결하고, 재치 있게 주고받을 수 있습니다. 메가스터디북스에서는 어휘 스도쿠를 통해 똑똑하게 의사소통하고, 다양한 어휘를 익힘으로써 공부의 기본기를 바로잡을 수 있도록 『머리에 콕 입에 착 붙는 어휘 스도쿠 속담』을 출간했습니다!<br>
초등학생이 꼭 알아야 할 필수 속담 100개를 재밌는 만화로 만나고, 신나는 공부 게임 어휘 스도쿠로 익힐 수 있지요. 또 말과 글 속에서 효과적으로 활용하는 법도 훈련할 수 있습니다.<br>
엘리하이 국어과 대표 강사, 맹지현 선생님이 초등 교과서와 일상생활에서 자주 쓰는 속담 100개를 중요도에 따라 선정하며 노하우를 꽉꽉 담았습니다. 그래서 이 책을 어휘 학습의 바이블로 자신있게 추천합니다.<br><br>

재미 만점, 효과 만점 어휘 스도쿠로<br>
어휘 실력은 저절로! 공부의 기본기는 두 배로!<br><br>

어린이의 눈높이에 딱 맞는 만화로 초등 필수 속담을 쉽게!<br>
어린이가 공감할 만한 일상 속 사례를 꼬불꼬불 라면 친구들의 만화를 통해 다채롭게 제시하여 언제, 어디서, 어떻게 속담을 쓰면 되는지 안내합니다. 여기저기 참견하기 좋아하는 장난꾸러기 라면 ‘뽀글이’, 맞는 말을 잘하는 만큼 오해도 많이 받는 대문자 T 짜장라면 ‘짜짜’, 쉽게 화를 내지만 금방 풀어지는 다혈질 요정 매운 볶음면 ‘매코미’ 그리고 고추, 파, 달걀 등 라면 친구들의 시선으로 속담이 필요한 상황을 재밌게 경험할 수 있지요. 속담의 의미와 함께 구체적인 사례, 비슷한말, 반대말까지 알차게 익힐 수 있습니다.<br><br>

어휘 스도쿠로 초등 필수 속담을 게임처럼 신나게!<br>
단순 따라 쓰기와 달리, 어휘 스도쿠는 어휘를 재밌고 효과적으로 익힐 수 있는 공부 게임입니다. 속담을 익힐 때 어린이가 가장 헷갈리는 부분을 어휘 스도쿠로 완성하도록 구성했지요. 심심할 때, 지겨울 때, 공부하기 싫을 때 3칸, 4칸, 5칸, 6칸 난이도별로 어휘 스도쿠에 도전하다 보면 집중력과 어휘력이 쑥쑥 자라게 됩니다. 어린이에게 ‘놀면서 익히는 자기 주도 학습’의 기쁨과 ‘게임처럼 신나는 공부’의 성취감을 선물할 것입니다.<br><br>

다양한 상황 속에서 직접 말하고, 써 보며 초등 필수 속담을 확실하게!<br>
어휘를 배웠지만 금세 잊어버리거나 상황에 맞게 제대로 써먹지 못하는 어린이를 위해 ‘말 속에서 써 보기’를 통해 대화 상황에서 활용하는 법을, ‘글 속에서 써먹기’, ‘내용에서 유추하기’로 다양한 갈래의 글에서 스스로 속담을 활용할 수 있도록 도왔습니다. 흥미진진한 퀴즈를 풀다 보면 눈 깜짝할 새 머리에 콕! 입에 착! 붙어 자연스럽게 속담이 나올 것입니다. 일상생활에서 학교 공부까지 속담이 필요한 어떤 상황에서든 이 책 하나면 문제없습니다.</p><br><br>
