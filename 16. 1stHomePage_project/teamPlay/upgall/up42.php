                  어린이 사회정서학습 전문가와 함께하는 내 마음과 친해지는 방법!<br><br>

최근 TV나 주변을 보면, 감정을 쉽게 폭발시키거나 과하게 자신을 깎아내리고, 감정을 표현하는 데 어려움을 겪는 아이들을 자주 볼 수 있습니다. <br>
이는 감정을 이해하고 다루는 방법을 배우지 못한 데서 비롯된 문제일 수 있습니다.<br>
그래서인지 최근 우리나라에서 ‘한국형 사회정서학습(Social and Emotional Learning, SEL)’ 프로그램을 점차 확대하고 있습니다. 
이제는 교육 현장에서도 마음에 대한 공부를 중요하게 여기기 시작했다는 것이죠. <br><br>

사실 미국, 캐나다, 영국 등에서는 이미 ‘사회정서학습’에 많은 투자를 해 왔습니다. <br>
사회정서학습이란 학생들이 자신의 감정을 잘 이해하고 관리하며, 타인과 긍정적으로 관계 맺고, 책임감 있는 결정을 내릴 수 있도록 돕는 교육 방법입니다. <br><br>

미국 시애틀에서 10년 넘게 영재 아이들을 대상으로 정서·감정·관계 교육을 지도해 온 사회정서학습 전문가이자,
베스트셀러 『결국 해내는 아이는 정서 지능이 다릅니다』의 저자인 김소연 선생님은 이런 문제 의식에서 『어린이 마음 수업』을 쓰게 되었습니다. <br><br>

이 책은 나의 행복과 건강한 관계 형성, 하고 싶은 일에 대한 도전, 책임 있는 선택을 하도록 돕는 사회정서학습 기반의 어린이 교양서입니다. <br>
『어린이 마음 수업』은 감정을 무조건 참거나 덮는 대신, 왜 그런 감정이 생기는지 정확히 알고 어떻게 표현하고 조절할 수 있는지를 아이 눈높이에 맞춰 풀어 낸 책입니다. <br>
저자는 이 책에 아이들이 자신을 더 잘 이해하고, 자신만의 속도로 건강하게 성장할 수 있도록 따뜻하고 실용적인 조언을 담았습니다. 
