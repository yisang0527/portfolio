                <h3>책 소개</h3>
                <p>
                 15년 경력 한의사가 10만 명 임상 결과로 증명한<br>
과학적인 다이어트 솔루션<br><br>

“다이어트는 의지력이 아니라 ‘시스템’이다!”<br>
다이어트 멘토 김희준 원장이 제안하는 ‘무노력 다이어트’의 모든 것<br><br>

힘든 식단, 고강도 운동, 반복되는 요요…다이어트에 실패한 모든 이를 위한 새로운 패러다임이 도착했다. 유튜브 ‘살빼남’으로 20만 구독자와 소통하며 임상 경험만 15년을 쌓아온 김희준 원장이 신간 《감량 혁명》을 통해 전하는 메시지는 단순하고도 강력하다. “다이어트는 노력으로 하는 게 아니다. 무노력 시스템으로 체질을 바꾸는 것이다.”<br><br>

이 책은 기존 다이어트 상식을 깨뜨리며, 대사적응, 에너지 소비 구조, 생물학적 특성 등 과학적 지식에 기반해 ‘살이 빠지는 몸’을 만드는 통합 전략을 제시한다. 단기 감량이나 단편적인 식단 대신, 누구나 일상에서 실천할 수 있는 ‘무노력 식단·운동·생활 습관’을 체계적으로 안내하며, 요요 없는 평생 감량 솔루션을 담았다. 지금껏 ‘왜 살이 안 빠졌는지’를 이해하고, ‘어떻게 하면 나만의 방식으로 자연스럽게 빠지는지’를 깨닫게 해주는 이 책은, 진짜 다이어트를 원하는 모든 이에게 반드시 필요한 안내서가 될 것이다.<br><br>

《감량 혁명》은 ‘적게 먹고 많이 움직이라’는 낡은 공식에서 벗어나, 각자의 생리적 특성과 생활 패턴에 맞춘 맞춤형 전략을 강조한다. 단순히 ‘이것만 먹으면 된다’는 식의 획일적인 접근이 아닌, 수면, 스트레스, 유산균, 미네랄까지 포함한 전방위적 조절을 통해 “노력하지 않아도 저절로 빠지는 몸”을 만드는 것을 목표로 한다. 다이어트를 공부해야 비로소 무노력이 가능하다는 저자의 주장은, 다이어트를 반복하는 사람들에게 새로운 전환점이 되어줄 것이다.
