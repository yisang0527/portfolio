지도, 사료, 도표 등 시각 자료를 풍부하게 담아 입체적 학습이 가능하도록 설계되었다. <br>
교과서 순서를 따르면서도 더 직관적인 흐름으로 재구성하여, 처음부터 끝까지 읽고 나면 한국사의 큰 줄기가 자연스럽게 그려진다.<br><br>

수능과 내신, 한국사능력검정시험 대비는 물론 성인 교양서로도 폭넓게 활용 가능하며, 수업 자료로 활용하기에도 적합한 책이다. 
