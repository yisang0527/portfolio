                 엄마의 손맛을 그대로 담은 50년 내공 사계절 한식<br><br>

*28만 구독자, 누적 조회수 5천만 유튜브 ‘이종임 스타일 채널’ 요리 노하우 대공개<br>
*계절별 식재료로 만드는 ‘때 되면 생각나는 제철 요리’<br>
*K-푸드 1세대 원조 요리 비법을 한 권에<br>
*전 요리 조리법 영상 QR코드 수록 - 이종임의 요리 수업 바로 시청 가능<br><br>

한그릇 요리나 퓨전 음식 등이 주를 이루는 요리책 시장에 오랜만에 나온 전통 한식 집밥 레시피북. 우리나라 가정식의 대가이자 현재 SNS 채널을 통해 다양한 세대와 소통하고 있는 요리연구가 이종임이 처음으로 낸 계절별 요리책이다. 1980년대 MBC [오늘의 요리]를 통해 최초로 ‘전 국민이 아는 요리 선생님’으로 유명세를 탄 이후 방송 활동, 후학 양성, 한식 해외 소개 등 다양한 분야에서 꾸준히 활약해온 저자는 얼마 전부터는 유튜브 ‘이종임 스타일 채널’을 열어 다른 요리 채널에서 찾아보기 어려운 우리나라의 귀한 한식 레시피를 소개하고 있다. 28만 명이 구독 중인 유튜브 콘텐츠 중에서 특히 반응이 좋았던 요리를 골라 계절별로 담은 이번 책은 베테랑 요리 전문가의 노하우와 유튜브로 대중과 소통하며 정리한 쉬운 레시피가 잘 결합되어 있다.<br><br>

우리나라는 사계절이 뚜렷하여 철마다 나오는 채소나 해산물 등이 각각 다르고, 이런 각 계절별로 나는 재료를 활용한 제철음식 역시 잘 발달되어 있다. 봄이면 생각나는 냉이된장국, 여름에 시원하게 먹는 오이냉국, 가을에 안 먹으면 서운한 전어회무침, 겨울에 특히 맛있는 동태찌개처럼 철마다 떠오르는 요리도 많다. 50년간 전통의 한식 맛을 지켜온 저자가 소개하는 제철요리 레시피는 제철재료로 만든 우리 음식만이 갖고 있는 맛과 영양을 알차게 담고 있다. 어릴 때 먹었던 엄마의 밥상을 떠오르게 하는 168개의 요리를 통해 요즘 다른 레시피북에서는 찾아보기 어려운 진짜 한식의 참맛을 즐길 수 있을 것이다.<br><br>

아울러 대표적인 계절별 식재료와 집에 만들어 두면 유용한 육수와 양념 만드는 법을 수록하고, 각 요리마다 배치된 QR코드를 통해 만드는 법을 동영상으로도 확인 가능하게 하여 한식 요리가 익숙하지 않은 사람들도 큰 어려움 없이 따라 해볼 수 있도록 하였다.<br><br>

